package D.Homeworkweek3;

import java.util.Scanner;

public class TableWhile {
    public static void main(String[] args) {

        Scanner sc=new Scanner(System.in);
        int n = sc.nextInt();
        int a=1;
        while (a<=10){
            System.out.println(a*n);
            a++;
        }
    }




}
